      <h2 style="margin-bottom:2%;">Használati útmutató</h2>
      <p style="text-align:justify;">A <b>Szerző választása</b> menüpontra kattintva kiválasztható az elemezni kívánt szerző, a megjelenő új oldalon pedig a szerzeménye.
          Az <b>Elküldés</b> gombra kattintva
      lefut a nyelvi elemzés, majd megjelennek a vizsgálat eredményei. Látható, hogy hány szót tartalmazott az elemzett szöveg, illetve a
      szavak száma egy adott szófajon belül. A program egy gyakorisági szótárat jelenít meg, amely az első 10 legtöbbet használt kifejezést
      tartalmazza. A generált szófelhő szintén a szavak gyakoriságát szemlélteti.</p>
